#include "main.h"

/**
 * reset_to_98 - Resets the value of an integer to 98
 * @n: Pointer to the integer to be reset
 *
 * This function takes a pointer to an integer and sets the value it
 * points to, to 98.
 */
void reset_to_98(int *n)
{
	*n = 98; /* Update the value pointed to by n to 98 */
}

